    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         if(Ename.getText().isEmpty()||Esurname.getText().isEmpty()||EstaffNo.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please enter all fields");
        }
        else
        {
             int selectedIndex=ChooseEmp.getSelectedIndex();
             employees.get(selectedIndex).setName(Ename.getText().trim());
             employees.get(selectedIndex).setSurname(Esurname.getText().trim());
             employees.get(selectedIndex).setStaffNr(Integer.parseInt(EstaffNo.getText().trim()));
             Job job=jobs.get(ChooseJ.getSelectedIndex());
             employees.get(selectedIndex).setJob(job);
            
            saveEmployeestoFile();
        } 
         
    }//GEN-LAST:event_jButton1ActionPerformed

