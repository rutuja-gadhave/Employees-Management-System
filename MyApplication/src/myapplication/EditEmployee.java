    private void ChooseEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChooseEmpActionPerformed
        // TODO add your handling code here:
        int selectedIndex=ChooseEmp.getSelectedIndex();
       
        Ename.setText(employees.get(selectedIndex).getName());
        Esurname.setText(employees.get(selectedIndex).getSurname());
        EstaffNo.setText(employees.get(selectedIndex).getStaffNr()+"");
       
        Job job=employees.get(selectedIndex).getJob();
        int index=0;
        
        for(int i=0;i<jobs.size();i++)
        { 

            if(jobs.get(i).equals(job))
            {
               
                index=i;
                break;
            }
        }
         ChooseJ.setSelectedIndex(index);
    }//GEN-LAST:event_ChooseEmpActionPerformed
    
